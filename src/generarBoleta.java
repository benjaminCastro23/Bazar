    private void btnIngresarBoletaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarBoletaActionPerformed
        // TODO add your handling code here:
        int cantidadProd = Integer.parseInt(txtCantidadProductos.getText());
        int total = Integer.parseInt(txtTotal.getText());
        int tipoVenta = 1;
        int totalIva = total+(total*19/100);
        
        Venta ven = new Venta(total, totalIva, tipoVenta);
        Boleta bol = new Boleta(cantidadProd, total);
        int estado1 = bol.agregarBoleta();
        int estado2 = ven.agregarVenta();
        if(estado1 > 0 && estado2 > 0){
            JOptionPane.showMessageDialog(rootPane, "Venta Registrada", "Ingresar Boleta", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(rootPane, "Error", "Hubo un error al ingresar la venta", JOptionPane.ERROR_MESSAGE);
        }
        
        
    }//GEN-LAST:event_btnIngresarBoletaActionPerformed

