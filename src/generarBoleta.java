    /**
     * Botón que se encarga de añadir productos al detalle de las boletas, al pulsar el botón
     * se hará una suma automatica de los productos, tanto de la cantidad a pagar como el precio total de estos.
     * También sumará automaticamente el id de la venta.
     * @param evt 
     */
    private void btnSiguienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSiguienteActionPerformed
        // TODO add your handling code here:
        int id_bol  = Integer.parseInt(txtIdBoleta.getText());
        int id_ven  = Integer.parseInt(txtIdVenta.getText());
        int id_pro  = Integer.parseInt(txtIdProducto.getText());
        int pUni  = Integer.parseInt(txtPrecioUnitario.getText());
        int cant  = Integer.parseInt(txtCantidad.getText());
        
        int pUnit  = Integer.parseInt(txtPrecioUnitario.getText());
        int total  = Integer.parseInt(txtTotal.getText());
        int cantProds  = Integer.parseInt(txtCantidadProductos.getText());
        int canti  = Integer.parseInt(txtCantidad.getText());
        int ventaSuma = 0;
        int cantSuma = 0;
        int totalSuma= 0;
        
        ventaSuma = id_ven + 1;
        totalSuma= total + (pUnit*canti);
        cantSuma = cantProds + canti;
        
        String siguienteVenta = Integer.toString(ventaSuma);
        String cantidadTotal = Integer.toString(cantSuma);
        String totalFinal = Integer.toString(totalSuma);
        
        txtIdVenta.setText(siguienteVenta);
        txtCantidadProductos.setText(cantidadTotal);
        txtTotal.setText(totalFinal);

        DetalleBoleta detbol = new DetalleBoleta(id_bol, id_ven, id_pro, pUni, cant);
        int estado = detbol.agregarDetalleBoleta();
        if(estado > 0){
            JOptionPane.showMessageDialog(rootPane, "Detalle Agregado", "Detalle Boleta", JOptionPane.INFORMATION_MESSAGE);
            txtIdProducto.setText("");
            txtPrecioUnitario.setText("");
            txtCantidad.setText("");
        } else {
            JOptionPane.showMessageDialog(rootPane, "Error", "Hubo un error al Insertar", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSiguienteActionPerformed

    
    
